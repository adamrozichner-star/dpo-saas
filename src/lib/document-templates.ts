// TEMPLATE 1: PRIVACY POLICY (תקנון פרטיות)
// ============================================
export function generatePrivacyPolicy(vars: DocumentVariables): string {
  const sensitiveDataWarning = vars.hasSensitiveData ? `
## מידע רגיש

אנו מעבדים סוגי מידע רגיש הכוללים: ${vars.sensitiveDataTypes.join(', ')}.

עיבוד מידע רגיש נעשה אך ורק:
- בהסכמתך המפורשת
- כאשר הדבר נדרש על פי חוק
- לצורך הגנה על אינטרסים חיוניים
- כאשר המידע פורסם על ידך באופן פומבי

אנו נוקטים באמצעי אבטחה מוגברים להגנה על מידע רגיש.
` : ''

  const internationalTransferSection = vars.internationalTransfer ? `
## העברת מידע לחו"ל

מידע אישי עשוי להיות מועבר ומאוחסן מחוץ לישראל. במקרים אלו:
- נוודא כי המדינה מספקת רמת הגנה נאותה
- נחתום על הסכמים חוזיים מתאימים
- נפעל בהתאם להנחיות הרשות להגנת הפרטיות

העברת מידע לחו"ל מתבצעת רק למדינות המאושרות על ידי הרשות להגנת הפרטיות או תחת הסכמים חוזיים מתאימים.
` : ''

  const thirdPartySharingSection = vars.thirdPartySharing ? `
## שיתוף מידע עם צדדים שלישיים

אנו עשויים לשתף מידע אישי עם:
- ספקי שירות הפועלים מטעמנו (עיבוד תשלומים, אחסון ענן, שירותי IT)
- יועצים מקצועיים (רואי חשבון, עורכי דין)
- רשויות ממשלתיות כנדרש על פי חוק

כל צד שלישי מחויב בהסכם לשמירה על סודיות המידע ואבטחתו.
` : `
## שיתוף מידע

איננו משתפים מידע אישי עם צדדים שלישיים, למעט כנדרש על פי חוק.
`

  return `# מדיניות פרטיות

**${vars.orgName}**
ח.פ./ע.מ.: ${vars.businessId}

תאריך עדכון אחרון: ${vars.generatedDate}

---

## מבוא

${vars.orgName} ("אנחנו", "שלנו", "החברה") מכבדת את פרטיות המשתמשים והלקוחות שלה. מדיניות פרטיות זו מתארת כיצד אנו אוספים, משתמשים, מאחסנים ומגנים על המידע האישי שלך, בהתאם לחוק הגנת הפרטיות, התשמ"א-1981 ותיקון 13 לחוק.

אנו מתחייבים לשמור על פרטיותך ולהגן על המידע האישי שלך.

---

## מידע שאנו אוספים

אנו אוספים את סוגי המידע הבאים:

${vars.dataTypes.map(type => `- ${dataTypeLabels[type] || type}`).join('\n')}

### מקורות המידע

${vars.dataSources.map(source => `- ${dataSourceLabels[source] || source}`).join('\n')}

---

## מטרות השימוש במידע

אנו משתמשים במידע האישי שלך למטרות הבאות:

${vars.processingPurposes.map(purpose => `- ${processingPurposeLabels[purpose] || purpose}`).join('\n')}

---

## הבסיס החוקי לעיבוד

עיבוד המידע האישי מתבצע על בסיס:
- **הסכמה** - כאשר נתת הסכמה מפורשת לעיבוד
- **חוזה** - כאשר העיבוד נדרש לביצוע חוזה עמך
- **חובה חוקית** - כאשר העיבוד נדרש על פי חוק
- **אינטרס לגיטימי** - לצורך הפעלת העסק שלנו

${sensitiveDataWarning}

---

${thirdPartySharingSection}

${internationalTransferSection}

---

## אבטחת מידע

אנו נוקטים באמצעי אבטחה מתאימים להגנה על המידע האישי שלך:

${vars.securityMeasures.filter(m => m !== 'none').map(measure => `- ${securityMeasureLabels[measure] || measure}`).join('\n')}

אנו מתחייבים לשמור על סודיות המידע ולמנוע גישה בלתי מורשית.

---

## שמירת מידע

אנו שומרים את המידע האישי שלך למשך הזמן הנדרש למטרות שלשמן נאסף, או כנדרש על פי חוק.

לאחר תום תקופת השמירה, המידע יימחק או יועבר לאנונימיזציה.

---

## הזכויות שלך

בהתאם לחוק הגנת הפרטיות, עומדות לך הזכויות הבאות:

### זכות עיון (סעיף 13)
הנך זכאי/ת לעיין במידע האישי שלך השמור במאגרינו.

### זכות תיקון (סעיף 14)
הנך זכאי/ת לבקש תיקון מידע שגוי או לא מדויק.

### זכות מחיקה (סעיף 14א)
הנך זכאי/ת לבקש מחיקת המידע האישי שלך, בכפוף להוראות החוק.

### זכות התנגדות
הנך זכאי/ת להתנגד לעיבוד מידע אישי בנסיבות מסוימות.

### הגשת בקשה
לממוש זכויותיך, ניתן לפנות אלינו באמצעות:
- אימייל: ${vars.dpoEmail}
- טלפון: ${vars.dpoPhone || '[יש להשלים]'}

נשיב לבקשתך תוך 30 יום כנדרש בחוק.

---

## ממונה הגנת הפרטיות

מינינו ממונה על הגנת הפרטיות (DPO) בהתאם לתיקון 13 לחוק:

**שם הממונה:** ${vars.dpoName}
**אימייל:** ${vars.dpoEmail}
**מספר רישיון:** ${vars.dpoLicense}

הממונה אחראי על:
- פיקוח על עמידה בדרישות החוק
- טיפול בפניות נושאי מידע
- ייעוץ והדרכה לעובדים
- קשר עם הרשות להגנת הפרטיות

---

## עוגיות (Cookies)

אתר האינטרנט שלנו עשוי להשתמש בעוגיות לצורך:
- תפעול תקין של האתר
- שיפור חווית המשתמש
- ניתוח שימוש באתר

באפשרותך לנהל את הגדרות העוגיות בדפדפן שלך.

---

## שינויים במדיניות

אנו עשויים לעדכן מדיניות זו מעת לעת. שינויים מהותיים יפורסמו באתר שלנו.

תאריך העדכון האחרון מופיע בראש מסמך זה.

---

## יצירת קשר

לשאלות או בירורים בנוגע למדיניות פרטיות זו:

**${vars.orgName}**
אימייל: ${vars.dpoEmail}
${vars.dpoPhone ? `טלפון: ${vars.dpoPhone}` : ''}

---

## הגשת תלונה

אם הנך סבור/ה כי נפגעה פרטיותך, באפשרותך להגיש תלונה לרשות להגנת הפרטיות:
- אתר: https://www.gov.il/he/departments/the_privacy_protection_authority
- טלפון: *3450

---

*מסמך זה נוצר באופן אוטומטי על ידי מערכת DPO-Pro ואושר על ידי ממונה הגנת הפרטיות.*
`
}

